const color = "black";

switch(color){
    case "blue": {
        console.log("The color is blue");
        break;
    }
    case "green": {
        console.log("The color is green");
        break;
    }
    case "black": {
        console.log("The color is black");
        break;
    }
    case "white": {
        console.log("The color is white");
        break;
    }
    default: {
        console.log("The color is red");
    }
}